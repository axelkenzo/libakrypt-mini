/*! \details В процессе инициализации контекста происходит развертывание ключевой информации.
    @param bctx Контекст алгоритма шифрования
    @param source Указатель на секретный ключ.
    @return Функция возвращает код ошибки или \ref ak_error_ok (в случае успеха)                   */
/* ----------------------------------------------------------------------------------------------- */
 int ak_bckey_context_create( ak_bckey bctx, ak_uint8 *source )
{
   int i, j, l, kdx = 2;
   ak_uint64 a0[2], a1[2], c[2], t[2], idx = 0;
   if(( bctx == NULL ) || ( source == NULL )) return ak_error_null_pointer;

 /* Инициализируем начальные значения */
   memset( bctx->direct, 0, sizeof( bctx->direct ));
   memset( bctx->reverse, 0, sizeof( bctx->reverse ));

   bctx->direct[2] = a0[0] = (( ak_uint64 *) source )[0];
   bctx->direct[3] = a0[1] = (( ak_uint64 *) source )[1];
   bctx->direct[0] = a1[0] = (( ak_uint64 *) source )[2];
   bctx->direct[1] = a1[1] = (( ak_uint64 *) source )[3];

 /* Теперь цикл для остальных пар */
  for( j = 0; j < 4; j++ ) {
     for( i = 0; i < 8; i++ ) {
      #ifdef LIBAKRYPT_LITTLE_ENDIAN
        c[0] = ++idx; /* вычисляем константу алгоритма согласно ГОСТ Р 34.12-2015 */
      #else
        c[0] = bswap_64( ++idx );
      #endif
        c[1] = 0;
        ak_kuznechik_linear_steps(( ak_uint8 *)c );

        t[0] = a1[0] ^ c[0]; t[1] = a1[1] ^ c[1];
        for( l = 0; l < 16; l++ ) ((ak_uint8 *)t)[l] = gost_pi[ ((ak_uint8 *)t)[l]];
        ak_kuznechik_linear_steps(( ak_uint8 *)t );

        t[0] ^= a0[0]; t[1] ^= a0[1];
        a0[0] = a1[0]; a0[1] = a1[1];
        a1[0] = t[0];  a1[1] = t[1];
     }
     kdx += 2;
     bctx->direct[kdx] = a1[0]; bctx->direct[kdx+1] = a1[1];

     kdx += 2;
     bctx->direct[kdx] = a0[0]; bctx->direct[kdx+1] = a0[1];
  }

 return ak_error_ok;
}

